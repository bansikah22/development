public class Cat {
    private String name;
    private int age;

    public Cat() {
        this.name = name;
        this.age = age;
    }

    // getter and setters
  //getters
    public String getName(){
        return name;
    }
    
    public int getAge(){
        return age;
    }

    //setters
    public void setName(){
        this.name = name;
    }
    public void setAge(){
        this.age = age;
    }
}